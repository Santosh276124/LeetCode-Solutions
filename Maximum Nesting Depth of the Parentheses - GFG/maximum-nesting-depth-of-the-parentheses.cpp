class Solution {
  public:
    int maxDepth(string s) {
        // code here
        int ans = 0;
        int cnt = 0;
        for(char &c : s)
        {
            if(c == '(')
             ans = max(ans, ++cnt);
            if(c == ')')
             cnt--;
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;

        Solution obj;
        int ans = obj.maxDepth(s);
        cout << ans << endl;
    }
    return 0;
}
